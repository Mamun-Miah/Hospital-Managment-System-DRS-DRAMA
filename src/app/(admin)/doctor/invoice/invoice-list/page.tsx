             swalWithBootstrapButtons.fire({
   
               title: "Deleted!",
               text: "Invoice Has been successfully deleted.",
               icon: "success",
               showConfirmButton: false,
               timer: 1500
             });
   //Sweet Alert for confirmation ends here
